public class PrimitiveDataType {

    public static void main(String[] args) {
        byte abyte=25;
        short bshort=1234;
        int cint=2898;
        long dlong=6365364;
        float efloat=7.2f;//6 decimal digit
        double fdouble=45.878;// 15 decimal digit
        char gchar='a';
        boolean hboolean=false;
        System.out.println(abyte);
        System.out.println(bshort);
        System.out.println(cint);
        System.out.println(dlong);
        System.out.println(efloat);
        System.out.println(fdouble);
        System.out.println(gchar);
        System.out.println(hboolean);

    }
}
